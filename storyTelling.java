package day5;

public class storyTelling {
    String storyName;
    String tellPeopleName;

    public storyTelling(String storyName, String tellPeopleName) {

        this.storyName = storyName;
        this.tellPeopleName = tellPeopleName;
    }

    public String getStoryName() {
        return storyName;
    }

    public String getTellPeopleName() {
        return tellPeopleName;
    }
}
